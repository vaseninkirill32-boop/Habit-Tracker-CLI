# ============================================================
# ТЕСТЫ ДЛЯ ТРЕКЕРА ПРИВЫЧЕК
# ============================================================
# Каждый тест проверяет одну часть программы
# Если тест проходит — выводим "ОК"
# Если нет — "ОШИБКА" и пояснение

print("=" * 40)
# Разделительная линия

print("ТЕСТЫ ТРЕКЕРА ПРИВЫЧЕК")
# Заголовок

print("=" * 40)
# Разделительная линия

all_ok = True
# all_ok — общий флаг "все тесты пройдены"
# True — пока ошибок нет
# Если какой-то тест не пройдёт — переключим на False


# ============================================================
# ТЕСТ 1: Создание привычки
# ============================================================
# Проверяем, что привычка создаётся правильно

print("\n1. Создание привычки...", end=" ")
# \n — перенос строки для отступа
# end=" " — не переводим строку, заканчиваем пробелом
# (результат "ОК" или "ОШИБКА" будет на той же строке)

habits = []
# Создаём пустой список (имитируем начало программы)

new_habit = {
    "name": "спорт",
    "streak": 0,
    "total": 0,
    "done_today": False
}
# Создаём словарь с тестовой привычкой
# Все поля как в основной программе

habits.append(new_habit)
# Добавляем привычку в список
# Имитируем команду "добавить спорт"

if len(habits) == 1 and habits[0]["name"] == "спорт":
    print("ОК")
    # Если в списке 1 элемент и его имя "спорт" — тест пройден
else:
    print("ОШИБКА")
    all_ok = False
    # Иначе — ошибка, общий флаг тоже в False

# ============================================================
# ТЕСТ 2: Отметка привычки
# ============================================================
# Проверяем, что отметка работает и счётчики увеличиваются

print("2. Отметка привычки...", end=" ")

habit = habits[0]
# Берём первую (и единственную) привычку из списка
# habit — это ссылка на словарь, изменения в нём отразятся в списке

habit["done_today"] = True
# Отмечаем как выполненную сегодня

habit["total"] += 1
# Увеличиваем общий счётчик на 1

habit["streak"] += 1
# Увеличиваем серию на 1

if habit["done_today"] and habit["total"] == 1 and habit["streak"] == 1:
    print("ОК")
    # Проверяем все три условия одновременно (and)
else:
    print("ОШИБКА")
    all_ok = False

# ============================================================
# ТЕСТ 3: Защита от повторной отметки
# ============================================================
# Проверяем, что нельзя отметить уже отмеченную привычку

print("3. Защита от повторной отметки...", end=" ")

already_done = habit["done_today"]
# Проверяем текущий статус (должен быть True после теста 2)

if already_done:
    print("ОК")
    # Если already_done == True — всё правильно, защита сработает
else:
    # Если вдруг False — имитируем ошибочную повторную отметку
    habit["done_today"] = True
    habit["total"] += 1
    habit["streak"] += 1
    print("ОШИБКА: повторная отметка прошла")
    all_ok = False

# ============================================================
# ТЕСТ 4: Сброс отметки
# ============================================================
# Проверяем, что можно снять отметку за сегодня

print("4. Сброс отметки...", end=" ")

habit["done_today"] = False
# Снимаем отметку

habit["total"] -= 1
# Уменьшаем общий счётчик (возвращаем как было)

habit["streak"] -= 1
# Уменьшаем серию (возвращаем как было)

if not habit["done_today"] and habit["total"] == 0 and habit["streak"] == 0:
    print("ОК")
    # Всё должно вернуться к 0
else:
    print("ОШИБКА")
    all_ok = False

# ============================================================
# ТЕСТ 5: Новый день — сброс серий у невыполненных
# ============================================================
# Проверяем логику нового дня:
# - streak сбрасывается только у неотмеченных привычек
# - done_today сбрасывается у всех

print("5. Новый день (сброс серий)...", end=" ")

habits = [
    {"name": "спорт", "streak": 5, "total": 10, "done_today": True},
    {"name": "чтение", "streak": 3, "total": 5, "done_today": False}
]
# Создаём новый список с двумя привычками:
# спорт — отмечена сегодня, серия 5
# чтение — НЕ отмечена, серия 3

for habit in habits:
    # Перебираем обе привычки

    if not habit["done_today"]:
        habit["streak"] = 0
    # Если не отмечена — сбрасываем серию

    habit["done_today"] = False
    # У всех сбрасываем отметку за сегодня

# Проверяем результаты
sport_ok = (habits[0]["streak"] == 5)
# У спорта серия должна остаться 5 (была отмечена)

read_ok = (habits[1]["streak"] == 0)
# У чтения серия должна сброситься в 0 (не была отмечена)

both_false = (not habits[0]["done_today"] and not habits[1]["done_today"])
# У обеих done_today должен быть False

if sport_ok and read_ok and both_false:
    print("ОК")
else:
    print("ОШИБКА")
    # Выводим подробности для отладки
    print("  спорт.streak = " + str(habits[0]["streak"]) + " (ожидалось 5)")
    print("  чтение.streak = " + str(habits[1]["streak"]) + " (ожидалось 0)")
    all_ok = False

# ============================================================
# ТЕСТ 6: Удаление привычки
# ============================================================
# Проверяем, что .pop() удаляет нужный элемент

print("6. Удаление привычки...", end=" ")

habits = [
    {"name": "спорт", "streak": 0, "total": 0, "done_today": False},
    {"name": "чтение", "streak": 0, "total": 0, "done_today": False}
]
# Создаём список из двух привычек

for i in range(len(habits)):
    # Перебираем индексы

    if habits[i]["name"] == "спорт":
        habits.pop(i)
        break
    # Нашли спорт — удаляем и выходим из цикла

if len(habits) == 1 and habits[0]["name"] == "чтение":
    print("ОК")
    # После удаления должен остаться 1 элемент — "чтение"
else:
    print("ОШИБКА")
    all_ok = False

# ============================================================
# ТЕСТ 7: Проверка на дубликаты при добавлении
# ============================================================
# Проверяем, что нельзя добавить привычку с тем же именем

print("7. Проверка на дубликаты...", end=" ")

habits = [{"name": "спорт"}]
# Список с одной привычкой

new_name = "спорт"
# Пытаемся добавить такую же

already_exists = False
for habit in habits:
    if habit["name"] == new_name:
        already_exists = True
        break
# Проверяем, есть ли уже такое имя

if already_exists:
    print("ОК")
    # Дубликат обнаружен — защита работает
else:
    print("ОШИБКА: дубликат не обнаружен")
    all_ok = False

# ============================================================
# ТЕСТ 8: Подсчёт статистики (выполненные сегодня)
# ============================================================
# Проверяем правильность подсчёта выполненных привычек

print("8. Подсчёт статистики...", end=" ")

habits = [
    {"name": "спорт", "done_today": True},
    {"name": "чтение", "done_today": False},
    {"name": "медитация", "done_today": True}
]
# Три привычки: две отмечены, одна нет

done_today = 0
for habit in habits:
    if habit["done_today"]:
        done_today += 1
# Считаем отмеченные

if done_today == 2:
    print("ОК")
    # Должно быть 2
else:
    print("ОШИБКА: выполнено " + str(done_today) + " (ожидалось 2)")
    all_ok = False

# ============================================================
# ТЕСТ 9: Проценты выполнения (для прогресс-бара)
# ============================================================
# Проверяем вычисление процентов

print("9. Проценты выполнения...", end=" ")

done = 2
total = 3
percent = done * 100 // total
# 2 * 100 = 200, 200 // 3 = 66 (целочисленное деление)

if percent == 66:
    print("ОК")
else:
    print("ОШИБКА: " + str(percent) + "% (ожидалось 66%)")
    all_ok = False

# ============================================================
# ТЕСТ 10: Сортировка пузырьком для топа
# ============================================================
# Проверяем, что сортировка по убыванию работает правильно

print("10. Сортировка для топа...", end=" ")

arr = [
    {"name": "а", "streak": 3},
    {"name": "б", "streak": 10},
    {"name": "в", "streak": 5}
]
# Три привычки с разными сериями: 3, 10, 5
# После сортировки должно быть: 10, 5, 3

# Сортировка пузырьком
for i in range(len(arr)):
    # Внешний цикл — проходы

    for j in range(len(arr) - 1 - i):
        # Внутренний цикл — сравнение соседей

        if arr[j]["streak"] < arr[j + 1]["streak"]:
            # Если левый меньше правого — меняем местами

            temp = arr[j]
            # Временная переменная для обмена

            arr[j] = arr[j + 1]
            arr[j + 1] = temp
            # Обмен местами

# Проверяем результат
if arr[0]["streak"] == 10 and arr[1]["streak"] == 5 and arr[2]["streak"] == 3:
    print("ОК")
    # Первый — 10, второй — 5, третий — 3
else:
    print("ОШИБКА")
    all_ok = False

# ============================================================
# ИТОГ# ============================================================
print()
print("=" * 40)

if all_ok:
    print("ВСЕ ТЕСТЫ ПРОЙДЕНЫ!")
else:
    print("ЕСТЬ ОШИБКИ!")

print("=" * 40)